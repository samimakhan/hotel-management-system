    private void cartButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cartButtonActionPerformed

     if(PoolButton.isSelected())
      s.AvailPool();
    if(ClubButton.isSelected())
      s.AvailClub();
    if(DineInButton.isSelected())
       s.AvailDineIn();
    if(FoodOrderingButton.isSelected())
       s.AvailFoodOrdering();
    if(RoomServiceButton.isSelected())
       s.AvailRoomService();
   

    try{
    FileWriter f = new FileWriter(new File("hotelServices.txt"), true);
    BufferedWriter bb = new BufferedWriter(f);
    bb.write("Customer ID: "); // find a way to get Member object involved here somewhere
    bb.newLine();
    bb.write(s.getServicesAvailed());
    bb.newLine();
    bb.write("***********");}
    catch (Exception e){
        System.out.println(e);
    }
    
 
    
    ServicesTotal st = new ServicesTotal(s);
   
    st.setVisible(true);
    st.setLocationRelativeTo(null);
    this.hide();
    
    }//GEN-LAST:event_cartButtonActionPerformed

